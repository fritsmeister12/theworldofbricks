                Artikel 4 - Het aanbod</h3>
            <br style="box-sizing:border-box;font-family:&quot;PT Sans&quot;,sans-serif;font-size:16px;">
            <ol
                style="box-sizing:border-box;margin-top:0px;margin-bottom:1rem;font-family:&quot;PT Sans&quot;,sans-serif;font-size:16px;">
                <br style="box-sizing:border-box">
                <li style="box-sizing:border-box">Indien een aanbod een beperkte geldigheidsduur heeft of onder voorwaarden
                    geschiedt, wordt dit nadrukkelijk in het aanbod vermeld.</li><br style="box-sizing:border-box">
                <li style="box-sizing:border-box">Het aanbod is vrijblijvend. De ondernemer is gerechtigd het aanbod te
                    wijzigen en aan te passen.</li><br style="box-sizing:border-box">
                <li style="box-sizing:border-box">Het aanbod bevat een volledige en nauwkeurige omschrijving van de
                    aangeboden producten en/of diensten. De beschrijving is voldoende gedetailleerd om een goede beoordeling
                    van het aanbod door de consument mogelijk te maken.
                    Als de ondernemer gebruik maakt van afbeeldingen zijn deze een waarheidsgetrouwe weergave van de
                    aangeboden producten en/of diensten. Kennelijke vergissingen of kennelijke fouten in het aanbod binden
                    de ondernemer niet.</li><br style="box-sizing:border-box">
                <li style="box-sizing:border-box">Alle afbeeldingen, specificaties gegevens in het aanbod zijn indicatie en
                    kunnen geen aanleiding zijn tot schadevergoeding of ontbinding van de overeenkomst.</li><br
                    style="box-sizing:border-box">
                <li style="box-sizing:border-box">Afbeeldingen bij producten zijn een waarheidsgetrouwe weergave van de
                    aangeboden producten. Ondernemer kan niet garanderen dat de weergegeven kleuren exact overeenkomen met
                    de echte kleuren van de producten.</li><br style="box-sizing:border-box">
                <li style="box-sizing:border-box">Elk aanbod bevat zodanige informatie, dat voor de consument duidelijk is

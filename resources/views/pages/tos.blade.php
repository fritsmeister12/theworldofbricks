                        <br style="box-sizing:border-box">
                        <li style="box-sizing:border-box">de prijs inclusief belastingen;</li><br
                            style="box-sizing:border-box">
                        <li style="box-sizing:border-box">de eventuele kosten van verzending;</li><br
                            style="box-sizing:border-box">
                        <li style="box-sizing:border-box">de wijze waarop de overeenkomst tot stand zal komen en welke
                            handelingen daarvoor nodig zijn;</li><br style="box-sizing:border-box">
                        <li style="box-sizing:border-box">het al dan niet van toepassing zijn van het herroepingsrecht;
                        </li><br style="box-sizing:border-box">
                        <li style="box-sizing:border-box">de wijze van betaling, aflevering en uitvoering van de
                            overeenkomst;</li><br style="box-sizing:border-box">

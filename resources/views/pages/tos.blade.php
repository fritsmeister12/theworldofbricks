                <li style="box-sizing:border-box">De consument kan een overeenkomst die voor bepaalde tijd is aangegaan en
                    die strekt tot het geregeld afleveren van producten (elektriciteit daaronder begrepen)&nbsp; of
                    diensten, te allen tijde tegen het einde van de bepaalde
                    duur opzeggen met inachtneming van daartoe overeengekomen opzeggingsregels en een opzegtermijn van ten
                    hoogste één maand.</li><br style="box-sizing:border-box">
                <li style="box-sizing:border-box">De consument kan de in de vorige leden genoemde overeenkomsten:<br
                        style="box-sizing:border-box">
                    <ul style="box-sizing:border-box;margin-top:0px;margin-bottom:0px">
                        <br style="box-sizing:border-box">
                        <li style="box-sizing:border-box">te allen tijde opzeggen en niet beperkt worden tot opzegging op
                            een bepaald tijdstip of in een bepaalde periode;</li><br style="box-sizing:border-box">
                        <li style="box-sizing:border-box">tenminste opzeggen op dezelfde wijze als zij door hem zijn
                            aangegaan;</li><br style="box-sizing:border-box">
                        <li style="box-sizing:border-box">altijd opzeggen met dezelfde opzegtermijn als de ondernemer voor
                            zichzelf heeft bedongen.</li><br style="box-sizing:border-box">
                    </ul>
                    <br style="box-sizing:border-box">
                </li><br style="box-sizing:border-box">
            </ol>
            <br style="box-sizing:border-box;font-family:&quot;PT Sans&quot;,sans-serif;font-size:16px;">
            <em style="box-sizing:border-box;font-family:&quot;PT Sans&quot;,sans-serif;font-size:16px;">Verlenging</em><br
                style="box-sizing:border-box;font-family:&quot;PT Sans&quot;,sans-serif;font-size:16px;">
            <ol
                style="box-sizing:border-box;margin-top:0px;margin-bottom:1rem;font-family:&quot;PT Sans&quot;,sans-serif;font-size:16px;">
                <br style="box-sizing:border-box">
                <li style="box-sizing:border-box">Een overeenkomst die voor bepaalde&nbsp; tijd is aangegaan en die strekt
                    tot het geregeld afleveren van producten (elektriciteit daaronder begrepen) of diensten, mag niet
                    stilzwijgend worden verlengd of vernieuwd voor een bepaalde
                    duur.</li><br style="box-sizing:border-box">
                <li style="box-sizing:border-box">In afwijking van het vorige lid mag een overeenkomst die voor
                    bepaalde&nbsp; tijd&nbsp; is aangegaan en die strekt tot het geregeld afleveren van dag- nieuws- en
                    weekbladen en tijdschriften stilzwijgend worden verlengd voor een bepaalde
                    duur van maximaal drie maanden, als de consument deze verlengde overeenkomst tegen het einde van de
                    verlenging kan opzeggen met een opzegtermijn van ten hoogste één maand.</li><br
                    style="box-sizing:border-box">
                <li style="box-sizing:border-box">Een overeenkomst die voor bepaalde tijd is aangegaan en die strekt tot
                    het geregeld afleveren van producten of diensten, mag alleen stilzwijgend voor onbepaalde duur worden
                    verlengd als de consument te allen tijde mag opzeggen
                    met een opzegtermijn van ten hoogste één maand en een opzegtermijn van ten hoogste drie maanden in geval
                    de overeenkomst strekt tot het geregeld, maar minder dan eenmaal per maand, afleveren van dag-, nieuws-
                    en weekbladen en tijdschriften.</li><br style="box-sizing:border-box">
                <li style="box-sizing:border-box">Een overeenkomst met beperkte duur tot het geregeld ter kennismaking
                    afleveren van dag-, nieuws- en weekbladen en tijdschriften (proef- of kennismakingsabonnement)&nbsp;
                    wordt niet stilzwijgend voortgezet en eindigt automatisch
                    na afloop van de proef- of kennismakingsperiode.</li><br style="box-sizing:border-box">
            </ol>
            <br style="box-sizing:border-box;font-family:&quot;PT Sans&quot;,sans-serif;font-size:16px;">
            <em style="box-sizing:border-box;font-family:&quot;PT Sans&quot;,sans-serif;font-size:16px;">Duur</em><br
                style="box-sizing:border-box;font-family:&quot;PT Sans&quot;,sans-serif;font-size:16px;">
            <ol
                style="box-sizing:border-box;margin-top:0px;margin-bottom:1rem;font-family:&quot;PT Sans&quot;,sans-serif;font-size:16px;">
                <br style="box-sizing:border-box">
                <li style="box-sizing:border-box">Als een overeenkomst een duur van meer dan een jaar heeft, mag de
                    consument na een jaar de overeenkomst te allen tijde met een opzegtermijn van ten hoogste een maand
                    opzeggen, tenzij de redelijkheid en billijkheid zich tegen
                    opzegging vóór het einde van de overeengekomen duur verzetten.</li><br style="box-sizing:border-box">
            </ol>
            <br style="box-sizing:border-box;font-family:&quot;PT Sans&quot;,sans-serif;font-size:16px;">
            <h3
                style="box-sizing:border-box;margin-top:0px;margin-bottom:0.5rem;font-family:Montserrat,sans-serif;font-weight:500;line-height:1.2;color:rgb(53,53,56);font-size:1.4rem;">
                Artikel 13 - Betaling</h3>
            <br style="box-sizing:border-box;font-family:&quot;PT Sans&quot;,sans-serif;font-size:16px;">
            <ol
                style="box-sizing:border-box;margin-top:0px;margin-bottom:1rem;font-family:&quot;PT Sans&quot;,sans-serif;font-size:16px;">
                <br style="box-sizing:border-box">
                <li style="box-sizing:border-box">Voor zover niet anders is overeengekomen, dienen de door de consument
                    verschuldigde bedragen te worden voldaan binnen 7 werkdagen na het ingaan van de bedenktermijn als
                    bedoeld in artikel 6 lid 1. In geval van een overeenkomst
                    tot het verlenen van een dienst, vangt deze termijn aan nadat de consument de bevestiging van de
                    overeenkomst heeft ontvangen.</li><br style="box-sizing:border-box">
                <li style="box-sizing:border-box">De consument heeft de plicht om onjuistheden in verstrekte of vermelde
                    betaalgegevens onverwijld aan de ondernemer te melden.</li><br style="box-sizing:border-box">
                <li style="box-sizing:border-box">In geval van wanbetaling van de consument heeft de ondernemer behoudens
                    wettelijke beperkingen, het recht om de vooraf aan de consument kenbaar gemaakte redelijke kosten in
                    rekening te brengen.</li><br style="box-sizing:border-box">
            </ol>
            <br style="box-sizing:border-box;font-family:&quot;PT Sans&quot;,sans-serif;font-size:16px;">
            <h3
                style="box-sizing:border-box;margin-top:0px;margin-bottom:0.5rem;font-family:Montserrat,sans-serif;font-weight:500;line-height:1.2;color:rgb(53,53,56);font-size:1.4rem;">
                Artikel 14 - Klachtenregeling</h3>
            <br style="box-sizing:border-box;font-family:&quot;PT Sans&quot;,sans-serif;font-size:16px;">
            <ol
                style="box-sizing:border-box;margin-top:0px;margin-bottom:1rem;font-family:&quot;PT Sans&quot;,sans-serif;font-size:16px;">
                <br style="box-sizing:border-box">
                <li style="box-sizing:border-box">De ondernemer beschikt over een voldoende bekend gemaakte
                    klachtenprocedure en behandelt de klacht overeenkomstig deze klachtenprocedure.</li><br
                    style="box-sizing:border-box">
                <li style="box-sizing:border-box">Klachten over de uitvoering van de overeenkomst moeten binnen 2 maanden
                    volledig en duidelijk omschreven worden ingediend bij de ondernemer, nadat de consument de gebreken
                    heeft geconstateerd.</li><br style="box-sizing:border-box">
                <li style="box-sizing:border-box">Bij de ondernemer ingediende klachten worden binnen een termijn van 14
                    dagen gerekend vanaf de datum van ontvangst beantwoord. Als een klacht een voorzienbaar langere
                    verwerkingstijd vraagt, wordt door de ondernemer binnen
                    de termijn van 14 dagen geantwoord met een bericht van ontvangst en een indicatie wanneer de consument
                    een meer uitvoerig antwoord kan verwachten.</li><br style="box-sizing:border-box">
                <li style="box-sizing:border-box">Indien de klacht niet in onderling overleg kan worden opgelost ontstaat

                Artikel 5 - De overeenkomst</h3>
            <br style="box-sizing:border-box;font-family:&quot;PT Sans&quot;,sans-serif;font-size:16px;">
            <ol
                style="box-sizing:border-box;margin-top:0px;margin-bottom:1rem;font-family:&quot;PT Sans&quot;,sans-serif;font-size:16px;">
                <br style="box-sizing:border-box">
                <li style="box-sizing:border-box">De overeenkomst komt, onder voorbehoud van het bepaalde in lid 4, tot
                    stand op het moment van aanvaarding door de consument van het aanbod en het voldoen aan de daarbij
                    gestelde voorwaarden.</li><br style="box-sizing:border-box">
                <li style="box-sizing:border-box">Indien de consument het aanbod langs elektronische weg heeft aanvaard,
                    bevestigt de ondernemer onverwijld langs elektronische weg de ontvangst van de aanvaarding van het
                    aanbod. Zolang de overeenkomst van deze aanvaarding
                    niet door de ondernemer is bevestigd, kan de consument de overeenkomst ontbinden.</li><br
                    style="box-sizing:border-box">
                <li style="box-sizing:border-box">Indien de overeenkomst elektronisch tot stand komt, treft de ondernemer
                    passende technische en organisatorische maatregelen ter beveiliging van de elektronische overdracht van
                    data en zorgt hij voor een veilige webomgeving.
                    Indien de consument elektronisch kan betalen, zal de ondernemer daartoe passende veiligheidsmaatregelen
                    in acht nemen.</li><br style="box-sizing:border-box">
                <li style="box-sizing:border-box">De ondernemer kan zich - binnen wettelijke kaders - op de hoogte stellen
                    of de consument aan zijn betalingsverplichtingen kan voldoen, evenals van al die feiten en factoren die
                    van belang zijn voor een verantwoord aangaan
                    van de overeenkomst op afstand. Indien de ondernemer op grond van dit onderzoek goede gronden heeft om
                    de overeenkomst niet aan te gaan, is hij gerechtigd gemotiveerd een bestelling of aanvraag te weigeren
